import React from 'react';

export const SuggestedResturant = ({ suggestedResturant, setSuggestedResturant }) => {
  const handleSuggestedResturantChange = (event) => {
    setSuggestedResturant(event.target.value);
  }

  return (

    <><h2>We therefor suggest you to book a table for two at {suggestedResturant}</h2><input
      type="text"
      onChange={handleSuggestedResturantChange}
      value={suggestedResturant} />
    </>
  );
}